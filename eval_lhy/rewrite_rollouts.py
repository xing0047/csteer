"""
Rewrite low-score (<= 0.6) rollouts in judge_results.json generated by generate_rollout_video.py.

Uses vLLM with Qwen2.5-72B-Instruct-AWQ to rewrite low-score rollouts so that their evaluation scores can be improved to > 0.6.

Supports two modes:
1. image: image mode
2. video: video mode

Example usage (image):
python rewrite_rollouts.py \
    --data_type image \
    --input_json refer_rollout_exp/qwen3vl/8b/judge_results.json \
    --output_json refer_rollout_exp/qwen3vl/8b/rewritten_rollouts.json \
    --judge_base_url http://localhost:8001/v1 \
    --judge_model_name Qwen/Qwen2.5-72B-Instruct-AWQ \
    --score_threshold 0.6 \
    --verbose

Example usage (video):
python rewrite_rollouts.py \
    --data_type video \
    --input_json ../ROLLOUT_RESULTS/video_rollout_n1024/judge_results.json \
    --output_json ../ROLLOUT_RESULTS/video_rollout_n1024/rewritten_rollouts.json \
    --judge_base_url http://localhost:8001/v1 \
    --judge_model_name Qwen/Qwen2.5-72B-Instruct-AWQ \
    --score_threshold 0.6 \
    --verbose
"""

import json
import argparse
import os
from typing import List, Dict, Any
from openai import OpenAI
from tqdm import tqdm
from utils.prompts import prompt_template


def parse_args():
    parser = argparse.ArgumentParser(
        description="Rewrite low-score rollouts to improve their accuracy"
    )
    parser.add_argument(
        "--data_type", type=str, choices=["image", "video"], default="image",
        help="Data type: image or video (default: image)"
    )
    parser.add_argument(
        "--input_json", type=str, required=True,
        help="Path to judge_results.json generated by generate_rollout_video.py"
    )
    parser.add_argument(
        "--output_json", type=str, required=True,
        help="Path to save the rewritten rollouts JSON file"
    )
    parser.add_argument(
        "--judge_base_url", type=str, default="http://localhost:8001/v1",
        help="vLLM service base URL for rewrite model (default: http://localhost:8001/v1)"
    )
    parser.add_argument(
        "--judge_model_name", type=str, default="Qwen/Qwen2.5-72B-Instruct-AWQ",
        help="Model name for rewriting rollouts (default: Qwen/Qwen2.5-72B-Instruct-AWQ)"
    )
    parser.add_argument(
        "--instruction", type=str, default=None,
        help="The instruction/question used for the original rollouts. If not provided, will use default based on data_type."
    )
    parser.add_argument(
        "--score_threshold", type=float, default=0.6,
        help="Score threshold below which rollouts will be rewritten (default: 0.6)"
    )
    parser.add_argument(
        "--verbose", action='store_true',
        help="Enable verbose output"
    )
    return parser.parse_args()


def rewrite_rollout_with_model(
    rewrite_client: OpenAI,
    rewrite_model_name: str,
    instruction: str,
    rollout_text: str,
    ground_truth: str,
    data_type: str = "image",
    verbose: bool = False
) -> str:
    """
    Rewrite a low-score rollout using the rewrite model.
    
    Args:
        rewrite_client: OpenAI client (connected to the rewrite model service)
        rewrite_model_name: Rewrite model name
        instruction: Original instruction/question
        rollout_text: Rollout text to be rewritten
        ground_truth: Ground-truth answer
        data_type: Data type ("image" or "video")
        verbose: Whether to print verbose logs
    
    Returns:
        str: Rewritten rollout text
    """
    # Use rollout_rewrite prompt from utils/prompts.py.
    # Prefer the prompt for the given data_type; fall back to "image" if missing.
    if "rollout_rewrite" in prompt_template:
        if data_type in prompt_template["rollout_rewrite"]:
            rewrite_prompt_template = prompt_template["rollout_rewrite"][data_type]
        elif "image" in prompt_template["rollout_rewrite"]:
            rewrite_prompt_template = prompt_template["rollout_rewrite"]["image"]
        else:
            raise ValueError(f"rollout_rewrite prompt for {data_type} not found in utils/prompts.py")
    else:
        raise ValueError("rollout_rewrite prompt not found in utils/prompts.py")
    
    # Build rewrite prompt
    full_prompt = rewrite_prompt_template.format(
        question=instruction,
        ground_truth=ground_truth,
        rollout_text=rollout_text
    )
    
    messages = [
        {
            "role": "user",
            "content": full_prompt,
        }
    ]
    
    try:
        # Choose different max_tokens based on data type.
        # - image: 1024 is typically sufficient
        # - video: requires more tokens to describe multiple frames and temporal changes
        if data_type == "video":
            max_tokens = 4096
        else:
            max_tokens = 1024
        
        response = rewrite_client.chat.completions.create(
            model=rewrite_model_name,
            messages=messages,
            temperature=0.2,  # Lower temperature for more stable rewrites
            max_tokens=max_tokens,
        )
        rewritten_text = response.choices[0].message.content.strip()
        
        if verbose:
            print(f"  Original: {rollout_text[:100]}...")
            print(f"  Rewritten: {rewritten_text[:100]}...")
        
        return rewritten_text
    except Exception as e:
        print(f"Error rewriting rollout: {e}")
        # If rewriting fails, fall back to the original text.
        return rollout_text


def rewrite_rollouts(
    input_json_path: str,
    output_json_path: str,
    judge_base_url: str,
    judge_model_name: str,
    instruction: str,
    data_type: str = "image",
    score_threshold: float = 0.6,
    verbose: bool = False
):
    """
    Load judge_results.json, rewrite low-score rollouts, and save results.
    
    Args:
        input_json_path: Path to the input judge_results.json
        output_json_path: Path to save rewritten results JSON
        judge_base_url: Rewrite model service base URL
        judge_model_name: Rewrite model name
        instruction: Original instruction/question
        data_type: Data type ("image" or "video")
        score_threshold: Score threshold; rollouts with score <= threshold will be rewritten
        verbose: Whether to print verbose logs
    """
    # Load input JSON
    print(f"Loading judge results from: {input_json_path}")
    with open(input_json_path, 'r', encoding='utf-8') as f:
        judge_results = json.load(f)
    
    print(f"Loaded {len(judge_results)} samples")
    
    # Connect to rewrite model service
    rewrite_client = OpenAI(base_url=judge_base_url, api_key="EMPTY", timeout=600)
    print(f"Connected to rewrite service at: {judge_base_url}")
    print(f"Using rewrite model: {judge_model_name}")
    print(f"Data type: {data_type}")
    print(f"Score threshold: {score_threshold} (rollouts with score <= {score_threshold} will be rewritten)")
    
    # Stats
    total_rollouts = 0
    low_score_rollouts = 0
    rewritten_rollouts = 0
    
    # Process each sample
    rewritten_results = []
    for sample_data in tqdm(judge_results, desc="Rewriting rollouts"):
        sample_id = sample_data['sample_id']
        media_name = sample_data.get('media_name') or sample_data.get('image_name', '')
        ground_truth = sample_data['ground_truth']
        original_rollouts = sample_data['rollouts']
        
        if verbose:
            print(f"\nProcessing sample {sample_id}: {media_name}")
        
        # Process each rollout
        rewritten_rollouts_list = []
        for rollout_data in original_rollouts:
            rollout_id = rollout_data['rollout_id']
            rollout_text = rollout_data['rollout_text']
            score = rollout_data['score']
            
            total_rollouts += 1
            
            if score <= score_threshold:
                low_score_rollouts += 1
                if verbose:
                    print(f"  Rewriting rollout {rollout_id} (score: {score:.2f})")
                
                rewritten_text = rewrite_rollout_with_model(
                    rewrite_client=rewrite_client,
                    rewrite_model_name=judge_model_name,
                    instruction=instruction,
                    rollout_text=rollout_text,
                    ground_truth=ground_truth,
                    data_type=data_type,
                    verbose=verbose
                )
                
                rewritten_rollouts_list.append({
                    'rollout_id': rollout_id,
                    'rollout_text': rewritten_text
                })
                rewritten_rollouts += 1
            else:
                rewritten_rollouts_list.append({
                    'rollout_id': rollout_id,
                    'rollout_text': rollout_text
                })
        
        rewritten_results.append({
            'sample_id': sample_id,
            'media_name': media_name,
            'ground_truth': ground_truth,
            'rollouts': rewritten_rollouts_list
        })
    
    # Save results to JSON
    print(f"\nSaving rewritten results to: {output_json_path}")
    output_dir = os.path.dirname(output_json_path)
    if output_dir and not os.path.exists(output_dir):
        os.makedirs(output_dir)
    
    with open(output_json_path, 'w', encoding='utf-8') as f:
        json.dump(rewritten_results, f, ensure_ascii=False, indent=2)
    
    # Print stats
    print(f"\n=== Rewrite Statistics ===")
    print(f"Total samples: {len(judge_results)}")
    print(f"Total rollouts: {total_rollouts}")
    print(f"Low-score rollouts (score <= {score_threshold}): {low_score_rollouts}")
    print(f"Successfully rewritten: {rewritten_rollouts}")
    print(f"Results saved to: {output_json_path}")
    
if __name__ == "__main__":
    args = parse_args()
    
    # Set default instruction based on data_type
    if args.instruction is None:
        if args.data_type == "video":
            instruction = "Please describe the whole video with IDs."
        else:
            instruction = "Please describe the whole image with IDs."
    else:
        instruction = args.instruction
    
    rewrite_rollouts(
        input_json_path=args.input_json,
        output_json_path=args.output_json,
        judge_base_url=args.judge_base_url,
        judge_model_name=args.judge_model_name,
        instruction=instruction,
        data_type=args.data_type,
        score_threshold=args.score_threshold,
        verbose=args.verbose
    )
